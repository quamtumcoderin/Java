class IfElse {
    public static void main(String[] args) {
        // Java Conditions and If Statements
        boolean isRaining = true;

        if (isRaining) {
            System.out.println("Bring an umbrella!");
        }

        // The if Statement

        if (20 > 18) {
            System.out.println("20 is greater than 18");
        }
    }
}